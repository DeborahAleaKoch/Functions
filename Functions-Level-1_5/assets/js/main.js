function math(number1, number2) {
  let multiply = number1 * number2;
  console.log(`Multiplikation von ${number1} und ${number2}: ${multiply}`);

  let division = number1 / number2;
  console.log(`Division von ${number1} und ${number2}: ${division}`);
}

math(10, 2);
math(30, 20);
math(100, 100);
math(5, 0);
math(45, 173);
math(1, 1000);
